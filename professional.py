def active_state(prof):
    result = 0
    result += prof["income"]["salary"]

    for i in prof["income"]["passive_income"]:
        result += prof["income"]["passive_income"][i][1]

    result -= prof["expenses"]["texes"]
    result -= prof["expenses"]["mortgage_house"]
    result -= prof["expenses"]["study"]
    result -= prof["expenses"]["car"]
    result -= prof["expenses"]["credit_card"]
    result -= prof["expenses"]["another"]
    result -= prof["expenses"]["bank_credit_pay_price"]

    result -= prof["expenses"]["child_price"] * prof["expenses"]["child_count"]

    return result
